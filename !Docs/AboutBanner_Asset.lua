camera = createcamera()
setposition(camera, 0, 12, -20)
setangle(camera, 20, 0)
setclscolor(camera, 255, 255, 255)
setambientlight(camera, 255, 255, 255)

cube = createcube()
setposition(cube, -10, 2.5, 0)
setsize(cube, 5, 5, 5)
setcolor(cube, 180, 20, 20)
sphere = createsphere()
setposition(sphere, -3, 3, 0)
setradius(sphere, 3)
setcolor(sphere, 20, 150, 20)
cylinder = createcylinder()
setposition(cylinder, 3, 2.5, 0)
setradius(cylinder, 2)
setheight(cylinder, 5)
setcolor(cylinder, 20, 50, 200)
tube = createtube()
setposition(tube, 8.5, 2.5, 0)
setradius(tube, 2)
setinnerradius(tube, 1)
setheight(tube, 5)
setcolor(tube, 180, 180, 20)

render(camera)